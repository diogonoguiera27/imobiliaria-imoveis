const SIDEBAR_COOKIE_NAME = "sidebar_state";
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7;
const SIDEBAR_WIDTH = "16rem";
const SIDEBAR_WIDTH_MOBILE = "18rem";
const SIDEBAR_WIDTH_ICON = "3rem";
const SIDEBAR_KEYBOARD_SHORTCUT = "b";

type SidebarContextProps = {
  state: "expanded" | "collapsed";
  open: boolean;
  setOpen: (open: boolean) => void;
  openMobile: boolean;
  setOpenMobile: (open: boolean) => void;
  isMobile: boolean;
  toggleSidebar: () => void;
};

const SidebarContext = React.createContext<SidebarContextProps | null>(null);

function useSidebar() {
  const context = React.useContext(SidebarContext);
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.");
  }

  return context;
}

function SidebarProvider({
  defaultOpen = false,
  open: openProp,
  onOpenChange: setOpenProp,
  className,
  style,
  children,
  ...props
}: React.ComponentProps<"div"> & {
  defaultOpen?: boolean;
  open?: boolean;
  onOpenChange?: (open: boolean) => void;
}) {
  const isMobile = useIsMobile();
  const [openMobile, setOpenMobile] = React.useState(false);

  // This is the internal state of the sidebar.
  // We use openProp and setOpenProp for control from outside the component.
  const [_open, _setOpen] = React.useState(defaultOpen);
  const open = openProp ?? _open;
  const setOpen = React.useCallback(
    (value: boolean | ((value: boolean) => boolean)) => {
      const openState = typeof value === "function" ? value(open) : value;
      if (setOpenProp) {
        setOpenProp(openState);
      } else {
        _setOpen(openState);
      }

      // This sets the cookie to keep the sidebar state.
      document.cookie = `${SIDEBAR_COOKIE_NAME}=${openState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`;
    },
    [setOpenProp, open]
  );

  // Helper to toggle the sidebar.
  const toggleSidebar = React.useCallback(() => {
    const newState = !open;
    setOpen(newState);
    setOpenMobile(newState);
    return newState;
  }, [open, setOpen, setOpenMobile]);

  // Adds a keyboard shortcut to toggle the sidebar.
  React.useEffect(() => {
    const handleKeyDown = (event: KeyboardEvent) => {
      if (
        event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
        (event.metaKey || event.ctrlKey)
      ) {
        event.preventDefault();
        toggleSidebar();
      }
    };

    window.addEventListener("keydown", handleKeyDown);
    return () => window.removeEventListener("keydown", handleKeyDown);
  }, [toggleSidebar]);

  // We add a state so that we can do data-state="expanded" or "collapsed".
  // This makes it easier to style the sidebar with Tailwind classes.
  const state = open ? "expanded" : "collapsed";

  const contextValue = React.useMemo<SidebarContextProps>(
    () => ({
      state,
      open,
      setOpen,
      isMobile,
      openMobile,
      setOpenMobile,
      toggleSidebar,
    }),
    [state, open, setOpen, isMobile, openMobile, setOpenMobile, toggleSidebar]
  );

  return (
    <SidebarContext.Provider value={contextValue}>
      <TooltipProvider delayDuration={0}>
        <div
          data-slot="sidebar-wrapper"
          style={
            {
              "--sidebar-width": SIDEBAR_WIDTH,
              "--sidebar-width-icon": SIDEBAR_WIDTH_ICON,
              ...style,
            } as React.CSSProperties
          }
          className={cn(
            "group/sidebar-wrapper has-data-[variant=inset]:bg-sidebar flex min-h-svh w-full",
            className
          )}
          {...props}
        >
          {children}
        </div>
      </TooltipProvider>
    </SidebarContext.Provider>
  );
}

function Sidebar({
  side = "left",
  // variant = "sidebar",
  collapsible = "offcanvas",
  className,
  children,
  ...props
}: React.ComponentProps<"div"> & {
  side?: "left" | "right";
  variant?: "sidebar" | "floating" | "inset";
  collapsible?: "offcanvas" | "icon" | "none";
}) {
  const { openMobile, setOpenMobile, open, setOpen } = useSidebar();

  if (collapsible === "none") {
    return (
      <div
        data-slot="sidebar"
        className={cn(
          "bg-sidebar text-sidebar-foreground flex h-full w-(--sidebar-width) flex-col",
          className
        )}
        {...props}
      >
        {children}
      </div>
    );
  }

  // Use Sheet component for all screen sizes
  return (
    <Sheet
      open={open || openMobile}
      onOpenChange={(isOpen) => {
        setOpenMobile(isOpen);
        setOpen(isOpen);
      }}
      {...props}
    >
      <SheetContent
        data-sidebar="sidebar"
        data-slot="sidebar"
        className="bg-sidebar text-sidebar-foreground w-(--sidebar-width) p-0 [&>button]:hidden"
        style={
          {
            "--sidebar-width": SIDEBAR_WIDTH_MOBILE,
          } as React.CSSProperties
        }
        side={side}
      >
        <SheetHeader className="sr-only">
          <SheetTitle>Sidebar</SheetTitle>
          <SheetDescription>Displays the sidebar.</SheetDescription>
        </SheetHeader>
        <div className="flex h-full w-full flex-col">{children}</div>
      </SheetContent>
    </Sheet>
  );
}


export default function SidebarTrigger() {
  const navigate = useNavigate();
  const location = useLocation();
  const { token, user } = useAuth();

  const goHome = () => {
    if (location.pathname !== "/home" || location.search) {
      navigate({ pathname: "/home", search: "" });
      window.dispatchEvent(new Event("clear-filters"));
    }
  };

  const ensureAuth = (path: string) => {
    if (!token) {
      navigate("/login", { state: { redirectTo: path } });
      return;
    }
    navigate(path);
  };

  const isAdmin = user?.role === "ADMIN";

  return (
    <>
      {/* ===== CABEÇALHO SUPERIOR ===== */}
      <header
        className="!fixed !z-50 !flex !h-[60px] !w-full 
                   !items-center  
                   !bg-gradient-to-r !from-red-400 !to-red-700 !shadow-xl"
      >
        <div className="!w-[95%] md:!w-[80%] !mx-auto !flex !items-center !justify-between">
          
          {/* ===== ESQUERDA (apenas Admin) ===== */}
          <div className="!flex !items-center !gap-6 !w-full">
            {isAdmin && (
              <nav className="hidden md:flex gap-6 items-center text-white text-sm font-semibold">
                <button
                  onClick={() => ensureAuth("/dashboard")}
                  className="hover:underline cursor-pointer"
                >
                  Dashboard
                </button>
                <button
                  onClick={() => ensureAuth("/admin/users")}
                  className="hover:underline cursor-pointer"
                >
                  Gerenciar Usuários
                </button>
              </nav>
            )}
          </div>

          {/* ===== DIREITA (Admin e User) ===== */}
          <div className="!flex !items-center !gap-6 !w-full !justify-end">
            <nav className="hidden md:flex gap-6 items-center text-white text-sm font-semibold">
              {/* Itens comuns a ambos */}
              <button
                onClick={goHome}
                className="hover:underline cursor-pointer"
              >
                Home
              </button>

              <button
                onClick={() => ensureAuth("/imovel/novo")}
                className="hover:underline cursor-pointer"
              >
                Cadastrar Imóveis
              </button>

              <button
                onClick={() => ensureAuth("/meus-imoveis")}
                className="hover:underline cursor-pointer"
              >
                Meus Imóveis
              </button>

              <Dialog>
                <DialogTrigger asChild>
                  <button className="hover:underline cursor-pointer">
                    Contato
                  </button>
                </DialogTrigger>
                <DialogContent className="max-w-lg">
                  <ContactInfoModal />
                </DialogContent>
              </Dialog>
            </nav>

            {/* Perfil do usuário (sempre visível) */}
            <div className="mr-2 sm:mr-4">
              <PerfilUsuarioModal />
            </div>
          </div>
        </div>
      </header>
    </>
  );
}




